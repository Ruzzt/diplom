#!/usr/bin/env python3
"""Генерация пояснительной записки — Глава: Титульный лист + Содержание + Введение."""

from docx import Document
from docx.shared import Pt, Cm
from docx.enum.text import WD_ALIGN_PARAGRAPH
import os

doc = Document()

# ==================== СТИЛИ ====================
style = doc.styles['Normal']
font = style.font
font.name = 'Times New Roman'
font.size = Pt(14)
style.paragraph_format.line_spacing = 1.5
style.paragraph_format.space_after = Pt(0)
style.paragraph_format.space_before = Pt(0)

for section in doc.sections:
    section.top_margin = Cm(2)
    section.bottom_margin = Cm(2)
    section.left_margin = Cm(3)
    section.right_margin = Cm(1.5)

def add_center(text, size=14, bold=False):
    p = doc.add_paragraph()
    p.alignment = WD_ALIGN_PARAGRAPH.CENTER
    run = p.add_run(text)
    run.font.name = 'Times New Roman'
    run.font.size = Pt(size)
    run.bold = bold
    return p

def add_right(text, indent=8, size=14):
    p = doc.add_paragraph()
    p.alignment = WD_ALIGN_PARAGRAPH.LEFT
    p.paragraph_format.left_indent = Cm(indent)
    run = p.add_run(text)
    run.font.name = 'Times New Roman'
    run.font.size = Pt(size)
    return p

def add_empty():
    p = doc.add_paragraph()
    return p

def add_heading_c(text, size=16):
    p = doc.add_paragraph()
    p.alignment = WD_ALIGN_PARAGRAPH.CENTER
    run = p.add_run(text)
    run.font.name = 'Times New Roman'
    run.font.size = Pt(size)
    run.bold = True
    p.paragraph_format.space_before = Pt(12)
    p.paragraph_format.space_after = Pt(6)
    return p

def add_para(text, bold=False):
    p = doc.add_paragraph()
    p.alignment = WD_ALIGN_PARAGRAPH.JUSTIFY
    p.paragraph_format.first_line_indent = Cm(1.25)
    run = p.add_run(text)
    run.font.name = 'Times New Roman'
    run.font.size = Pt(14)
    run.bold = bold
    return p

# ==================== ТИТУЛЬНЫЙ ЛИСТ ====================
add_center('МИНИСТЕРСТВО НАУКИ И ВЫСШЕГО ОБРАЗОВАНИЯ\nРОССИЙСКОЙ ФЕДЕРАЦИИ')
add_empty()
add_center('НАЦИОНАЛЬНЫЙ ИССЛЕДОВАТЕЛЬСКИЙ\nМОСКОВСКИЙ ГОСУДАРСТВЕННЫЙ СТРОИТЕЛЬНЫЙ УНИВЕРСИТЕТ', bold=True)
add_empty()
add_center('Кафедра/структурное подразделение ИСТАС')
add_empty()
add_empty()
add_center('ВЫПУСКНАЯ КВАЛИФИКАЦИОННАЯ РАБОТА', size=16, bold=True)
add_empty()
add_center('на тему:')
add_center('«Разработка модуля аутентификации и управления правами доступа\nк корпоративной системе на основе распознавания лиц»', bold=True)
add_empty()
add_empty()
add_right('Направление подготовки:\nИнформационные системы и технологии')
add_empty()
add_right('Профиль:\nИнформационные системы и технологии\nв строительстве')
add_empty()
add_right('Научный руководитель:\nКнязева Н.В.')
add_empty()
add_empty()
add_empty()
add_center('Москва — 2026')

doc.add_page_break()

# ==================== СОДЕРЖАНИЕ ====================
add_heading_c('СОДЕРЖАНИЕ')
add_empty()

contents = [
    'Введение',
    '1. Аналитический обзор существующих подходов к аутентификации и управлению правами доступа на основе распознавания лиц',
    '  1.1 Анализ тенденций информационной безопасности и методов аутентификации',
    '  1.2 Обзор алгоритмов распознавания лиц и систем управления доступом',
    '  1.3 Анализ существующих решений биометрической аутентификации и нормативно-правовой базы',
    '2. Методология решения задачи аутентификации и управления правами доступа на основе распознавания лиц и её обеспечение',
    '  2.1 Методология разработки модуля биометрической аутентификации и RBAC-системы',
    '  2.2 Программное обеспечение',
    '  2.3 Аппаратное обеспечение',
    '  2.4 Техническое и информационное обеспечение',
    '3. Проектирование и разработка защищённого модуля аутентификации и управления правами доступа на основе распознавания лиц',
    '  3.1 Схема архитектуры системы и алгоритмы работы',
    '  3.2 Разработка модуля распознавания лиц на основе face-api.js',
    '  3.3 Реализация RBAC-системы и REST API для интеграции с корпоративными системами',
    '  3.4 Математическая модель',
    '  3.5 Разработка алгоритма регистрации и аутентификации пользователя',
    '4. Апробация предложенных решений на конкретном примере',
    '  4.1 Апробация решения на конкретном примере',
    '  4.2 Оценка эффективности внедрения. Технико-экономические показатели',
    '  4.3 Инструкция пользователя',
    'Заключение',
    'Список использованных источников',
]

for title in contents:
    p = doc.add_paragraph()
    run = p.add_run(title)
    run.font.name = 'Times New Roman'
    run.font.size = Pt(14)
    if not title.startswith('  '):
        run.bold = True

doc.add_page_break()

# ==================== ВВЕДЕНИЕ ====================
add_heading_c('ВВЕДЕНИЕ')

add_para('В современных условиях цифровой трансформации строительной отрасли вопросы информационной безопасности корпоративных систем приобретают особую актуальность. Строительные организации оперируют значительными объёмами конфиденциальной информации: проектной документацией, сметами, финансовыми данными, персональными данными сотрудников. Несанкционированный доступ к этим данным может повлечь серьёзные финансовые и репутационные потери.')

add_para('Традиционные методы аутентификации, основанные на парольной защите, демонстрируют свою недостаточную эффективность. По данным отчёта Verizon Data Breach Investigations Report за 2024 год, более 80% инцидентов утечки данных связано с компрометацией учётных данных. Биометрическая аутентификация, в частности распознавание лиц, представляет собой перспективную альтернативу, обеспечивающую более высокий уровень безопасности при сохранении удобства для пользователя.')

add_para('Актуальность данной работы обусловлена следующими факторами:')
add_para('— необходимостью повышения уровня информационной безопасности корпоративных систем в строительной отрасли;')
add_para('— ростом числа кибератак на предприятия строительного комплекса;')
add_para('— развитием технологий компьютерного зрения и машинного обучения, позволяющих реализовать надёжные системы биометрической аутентификации;')
add_para('— требованиями нормативно-правовых актов в области защиты персональных данных (Федеральный закон №152-ФЗ «О персональных данных», ГОСТ Р 54412-2011);')
add_para('— отсутствием специализированных решений для строительной отрасли, учитывающих ролевую модель доступа с биометрической верификацией.')

add_para('Целью выпускной квалификационной работы является разработка модуля аутентификации и управления правами доступа к корпоративной системе управления строительными проектами «СтройКонтроль» на основе распознавания лиц.')

add_para('Для достижения поставленной цели необходимо решить следующие задачи:')
add_para('1) провести аналитический обзор существующих методов аутентификации и алгоритмов распознавания лиц;')
add_para('2) проанализировать существующие решения биометрической аутентификации и нормативно-правовую базу;')
add_para('3) разработать архитектуру системы биометрической аутентификации с ролевой моделью доступа (RBAC);')
add_para('4) реализовать модуль распознавания лиц на основе библиотеки face-api.js с использованием нейросетевых моделей;')
add_para('5) реализовать механизм проверки «живости» (liveness detection) на основе распознавания жестов с использованием MediaPipe;')
add_para('6) разработать REST API для интеграции модуля аутентификации с корпоративной системой;')
add_para('7) реализовать систему журналирования действий (аудит) и назначения пользователей на проекты;')
add_para('8) провести апробацию разработанного решения и оценить его эффективность.')

add_para('Объектом исследования является процесс аутентификации и управления правами доступа пользователей к корпоративным информационным системам строительных организаций.')

add_para('Предметом исследования выступают методы и технологии биометрической аутентификации на основе распознавания лиц и ролевого управления доступом.')

add_para('Научная новизна работы заключается в комплексном подходе к обеспечению безопасности корпоративной системы, сочетающем биометрическую аутентификацию по лицу, верификацию «живости» через распознавание жестов, ролевую модель управления доступом (RBAC) с шестью ролями, повторную биометрическую верификацию при выполнении критических операций и журналирование всех действий пользователей.')

add_para('Практическая значимость работы определяется возможностью внедрения разработанного модуля в корпоративные информационные системы строительных организаций для повышения уровня информационной безопасности и контроля доступа к проектной документации, сметам и другим конфиденциальным данным.')

# ==================== СОХРАНЕНИЕ ====================
output_path = os.path.join(os.path.dirname(os.path.abspath(__file__)), 'Пояснительная_записка.docx')
doc.save(output_path)
print(f'Сохранено: {output_path}')
